package com.seekon.smartclient.framework.exception;


public class ToolBarComponentNotFoundException extends Exception {

	private static final long serialVersionUID = -1014513999010983538L;

	public ToolBarComponentNotFoundException() {
		super();
	}

	public ToolBarComponentNotFoundException(String message) {
		super(message);
	}

	public ToolBarComponentNotFoundException(String message, Throwable cause) {
		super(message, cause);
	}

	public ToolBarComponentNotFoundException(Throwable cause) {
		super(cause);
	}

}
