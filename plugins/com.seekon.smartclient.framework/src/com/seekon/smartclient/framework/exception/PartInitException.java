package com.seekon.smartclient.framework.exception;

public class PartInitException extends Exception {

	private static final long serialVersionUID = -8765230972746151402L;

	public PartInitException() {
		super();
	}

	public PartInitException(String message, Throwable cause) {
		super(message, cause);
	}

	public PartInitException(String message) {
		super(message);
	}

	public PartInitException(Throwable cause) {
		super(cause);
	}

}
